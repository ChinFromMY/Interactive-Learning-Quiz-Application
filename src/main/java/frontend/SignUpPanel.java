    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        // Store username and password in csv file
        String inputUsername;
        String inputPassword;

        // get input string
        inputUsername = usernameField.getText();
        inputPassword = new String(passwordField.getPassword());
        
        
        // Validate empty input
        if (inputUsername.isEmpty() || inputPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and password cannot be empty.","Input error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            // Verify credentials
            if (UserData.validateCredentials(inputUsername, inputPassword)) {
                // if account exists
                JOptionPane.showMessageDialog(this, "This account already existed. Please try again or login.", "Error", JOptionPane.ERROR_MESSAGE);

            } else { // sign up for new user
                // Save to dataset
                UserData.saveCredentials(inputUsername, inputPassword);

                // Save to UserData class for later use
                UserData.setUsername(inputUsername);
                UserData.setPassword(inputPassword);

                // Show welcome message if account existed
                JOptionPane.showMessageDialog(this, "Welcome aboard, " + inputUsername + "!");

                MenuPanel menuPanel = new MenuPanel();
                menuPanel.setMainPanel(mainPanel);  
                mainPanel.add(menuPanel, "menu");   
                // Switch to MenuPanel
                CardLayout card = (CardLayout)(mainPanel.getLayout());
                card.show(mainPanel,"menu");
            }
        } catch (UserDataException e) {
            JOptionPane.showMessageDialog(this, 
                "An error occurred for signing up:\n" + e.getMessage(),
                "Sign Up Error", 
                JOptionPane.ERROR_MESSAGE);
        }
        
        usernameField.setText("");
        passwordField.setText("");
    }//GEN-LAST:event_startButtonActionPerformed

