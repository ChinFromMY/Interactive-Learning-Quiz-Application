    @Override
    public void displayQuestion() {
        // If quiz completed
        if (currentIdx >= quiz4.getQuestion().size()) {
                return;
        }
        
        // Assign each question to Question class instance
        Question q = quiz4.getQuestion().get(currentIdx);
        
        // Get topic
        topicLabel.setText("Quiz " + quiz4.getTopic());
        
        // Get score
        scoreLabel.setText("Score " + quiz4.getScore());
        // Get question
        questionTextArea.setText(q.getQuestionText());
        questionNoLabel.setText("Question " + (currentIdx+1) + " / " + quiz4.getQuestion().size());
        // Get choices
        java.util.List<String> choices = q.getChoices();
        opt1.setText(choices.get(0));
        opt2.setText(choices.get(1));
        opt3.setText(choices.get(2));
        opt4.setText(choices.get(3));
    }   
    
    @Override
    public void updateTimerLabel(long seconds) {
        timerLabel.setText("Time " + seconds + "s");
    }
    
    @Override
    public void startTimer() {
        try {
            if (timer4 != null) {
                timer4.startTimer();  // can throw TimerException if already running
            }
        } catch (TimerException ex) {
            JOptionPane.showMessageDialog(null, "Error starting timer: " + ex.getMessage(), "Timer Start Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    @Override
    public void stopTimer() {
        timer4.stopTimer();
    }
    
    @Override
    public void resetQuiz() {
        System.out.println("MCQ Panel, Quiz resetting!");
        currentIdx = 0;
        selected = "";
        quiz4.setScore(0);
        updateTimerLabel(0);
        
        try {
            startTimer();
            // Restart if needed
            this.quiz4 = new Quiz4();
            // Clear previous selection
            buttonGroup1.clearSelection();
            // Re-display first question
            displayQuestion();

            revalidate();
            repaint();
            //startTimer();
        } catch (QuizException e) {
            JOptionPane.showMessageDialog(this, "Failed to reset quiz: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
