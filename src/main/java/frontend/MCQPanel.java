    private void nextQuestionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextQuestionBtnActionPerformed
        // Get selection
        if (opt1.isSelected()) selected = "A";
        else if (opt2.isSelected()) selected = "B";
        else if (opt3.isSelected()) selected = "C";
        else if (opt4.isSelected()) selected = "D";
        
        // If not answer selected
        if (selected.equals("")) {
            JOptionPane.showMessageDialog(this, "Please select an option!");
            return;
        } 
        // Get question
        Question currentQuestion = quiz4.getQuestion().get(currentIdx);
        
        // Check answer
        System.out.println("mcqquiz, check answer...");
        if (quiz4.checkAnswer(currentQuestion, selected)) {
            System.out.println("Option selected is correct: " + selected);
            JOptionPane.showMessageDialog(this, "Correct! +10");
            
        } else {
            System.out.println("Option selected is wrong: " + selected);
            JOptionPane.showMessageDialog(this, "Wrong!");
            
        }
        
        selected = "";
        buttonGroup1.clearSelection();
        // Display next question
        currentIdx++;
        
        if (currentIdx < quiz4.getQuestion().size()) {
            displayQuestion();
        } else {
            // Quiz completed
            System.out.println("MCQ Panel, Quiz end!");
            // TImer stop
            stopTimer();
            setTimeTaken(timer4.getSeconds());
           
            // Result display
            // Switch to ResultPanel
            CardLayout card = (CardLayout) mainPanel.getLayout();
            card.show(mainPanel, "result");
          //  JOptionPane.showMessageDialog(this,"Your score: "+ quiz4.getScore());
         //   JOptionPane.showMessageDialog(this, "Quiz Completed!");
            try {
                ResultLog.saveResult(4, quiz4.getScore(), timeTaken, quiz4.getCorrectCnt(), quiz4.getWrongCnt());
                resultPanel.displayResult(quiz4.getScore(), quiz4.getQuestion().size(), timeTaken, quiz4.getCorrectCnt(), quiz4.getWrongCnt(), quiz4.getTopic());
            } catch (ResultException e) {
                    JOptionPane.showMessageDialog(this, 
                        "An error occurred for saving result:\n" + e.getMessage(),
                        "Result Saving Error", 
                        JOptionPane.ERROR_MESSAGE);
                }
            
        }
    }//GEN-LAST:event_nextQuestionBtnActionPerformed


