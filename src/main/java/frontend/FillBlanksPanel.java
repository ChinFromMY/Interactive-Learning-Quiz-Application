    @Override
    public void displayQuestion() {
        // If quiz completed
        if (currentIdx >= quiz1.getQuestion().size()) {
                return;
        }
        
        // Assign each question to Question class instance
        Question q = quiz1.getQuestion().get(currentIdx);
        
        // Get topic
        topicLabel.setText("Quiz " + quiz1.getTopic());
        // Get score
        scoreLabel.setText("Score " + quiz1.getScore());
        // Get question
        questionTextArea.setText(q.getQuestionText());
        questionNoLabel.setText("Question " + (currentIdx+1) + " / " + quiz1.getQuestion().size());
        
    }   
    
    @Override
    public void updateTimerLabel(long seconds) {
        timerLabel.setText("Time " + seconds + "s");
    }
    
    @Override
    public void startTimer() {
        try {
            if (timer1 != null) {
                timer1.startTimer();  // can throw TimerException if already running
            }
        } catch (TimerException ex) {
            JOptionPane.showMessageDialog(null, "Error starting timer: " + ex.getMessage(), "Timer Start Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    @Override
    public void stopTimer() {
        timer1.stopTimer();
    }
    
    @Override
    public void resetQuiz() {
        currentIdx = 0;
        blanksAns = "";
        ansField.setText("");
        quiz1.setScore(0);
        updateTimerLabel(0);
        
        try {
            // Restart if needed
            startTimer();
            this.quiz1 = new Quiz1();
            // Re-display first question
            displayQuestion();

            revalidate();
            repaint();
            //startTimer();
        } catch (QuizException e) {
            JOptionPane.showMessageDialog(this, "Failed to reset quiz: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
