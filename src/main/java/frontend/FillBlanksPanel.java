    private void nextQuestionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextQuestionBtnActionPerformed
       
        // Get user answer
        blanksAns = ansField.getText().trim();
       
        // Validate empty input
        if (blanksAns.isEmpty() || blanksAns.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Your answer cannot be empty.","Input error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Get question
        Question currentQuestion = quiz1.getQuestion().get(currentIdx);

        // Check answer
        System.out.println("fbquiz, check answer...");
        if (quiz1.checkAnswer(currentQuestion, blanksAns)) {
            System.out.println("Input answer is correct: " + blanksAns);
            JOptionPane.showMessageDialog(this, "Correct! +10");

        } else {
            System.out.println("Input answer is wrong: " + blanksAns);
            JOptionPane.showMessageDialog(this, "Wrong!");

        }
        
        ansField.setText("");
        // Display next question
        currentIdx++;

        if (currentIdx < quiz1.getQuestion().size()) {
            displayQuestion();
        } else {
            // Quiz completed
            // TImer stop
            stopTimer();
            setTimeTaken(timer1.getSeconds());
            // Result display
            // Switch to ResultPanel
            CardLayout card = (CardLayout) mainPanel.getLayout();
            card.show(mainPanel, "result");
            
            try {
                ResultLog.saveResult(1, quiz1.getScore(), timeTaken, quiz1.getCorrectCnt(), quiz1.getWrongCnt());
                resultPanel.displayResult(quiz1.getScore(), quiz1.getQuestion().size(), timeTaken, quiz1.getCorrectCnt(), quiz1.getWrongCnt(), quiz1.getTopic());
            } catch (ResultException e) {
                    JOptionPane.showMessageDialog(this, 
                        "An error occurred for saving result:\n" + e.getMessage(),
                        "Result Saving Error", 
                        JOptionPane.ERROR_MESSAGE);
                }
            
        }

    }//GEN-LAST:event_nextQuestionBtnActionPerformed

